# Ruby program to declare and output variables

# Declare variables
x = 100
y = 202.5
s = "Apples and oranges"

# Output variables and their class types
puts "x:       #{x}"
puts "x.class: #{x.class}\n\n"

puts "y:       #{y}"
puts "y.class: #{y.class}\n\n"

puts "s:       #{s}"
puts "s.class: #{s.class}\n\n"

# Output string 4 times
4.times { puts "Hello World!" }
